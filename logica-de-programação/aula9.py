# Operadores aritméticos (matemática)

adicao = 10 + 10
print('Adição', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

divisao = 10 / 2.2 # float
print('Divisão', divisao)

divisao_inteira = 10 // 2.2 # todo número após o ponto, ele não irá colocar as casas decimais
print('Divisão inteira', divisao_inteira)

exponeciacao = 2 ** 10
print('Exponeciação', exponeciacao)

modulo = 55 % 5 # resto da divisão
print('Módulo', modulo)
