# Tipo de dado bool (boolean)
# Ao quetionar algo em um programa, só existem duas respostas possíveis:
# sim (True) ou não (False)
# Existem vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que questiona se um valor é igual a outro

print(10 == 10) # True
print(10 == 3) # False
print(10 != 8) # Diferente de (True)

print(10 <= 5) # Menor ou igual a (False)
print(10 >= 5) # Maior ou igual a (True)

